DEFAULT_MAIN_CATEGORY = "Τρόφιμα"
DEFAULT_SUB_CATEGORY = "Μπαχαρικά & Αλάτι"


def get_subcategories(main_category: str) -> list[str]:
    """Return subcategories for a given main category."""
    return CATEGORIES.get(main_category, [])


def validate_category(main_category: str, sub_category: str) -> tuple[str, str]:
    """Validate and normalise a (mainCategory, subCategory) pair.
    Returns corrected values, falling back to defaults if invalid.
    """
    if main_category not in CATEGORIES:
        return DEFAULT_MAIN_CATEGORY, DEFAULT_SUB_CATEGORY
    subs = CATEGORIES[main_category]
    if sub_category not in subs:
        return main_category, subs[0] if subs else DEFAULT_SUB_CATEGORY
    return main_category, sub_category


def build_ai_category_list() -> str:
    """Build a compact numbered list of all categories for AI prompts."""
    lines: list[str] = []
    for main, subs in CATEGORIES.items():
        lines.append(f"• {main}:")
        for sub in subs:
            lines.append(f"    - {sub}")
    return "\n".join(lines)
